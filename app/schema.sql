CREATE TABLE REDACTION_RUNS (
  RUN_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  INPUT_TEXT CLOB NOT NULL,
  REDACTED_TEXT CLOB NOT NULL,
  OPTIONS_JSON CLOB,
  COUNTS_JSON CLOB,
  FINDINGS_JSON CLOB,
  INPUT_FILE_BLOB BLOB,
  REDACTED_FILE_BLOB BLOB,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);
